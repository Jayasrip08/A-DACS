// Global Key for Navigator to perform navigation from anywhere
final GlobalKey<NavigatorState> navigatorKey = GlobalKey<NavigatorState>();

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  
  // Initialize Firebase with error handling
  try {
    await Firebase.initializeApp(
      options: DefaultFirebaseOptions.currentPlatform,
    );

    // FIX: Initialize App Check only where supported or configured
    if (!kIsWeb) {
      await FirebaseAppCheck.instance.activate(
        androidProvider: AndroidProvider.debug,
        appleProvider: AppleProvider.deviceCheck,
      );
    } else {
      // For Web, App Check requires a ReCaptcha site key. 
      // Skipping for now to allow development on Chrome.
      print("Firebase App Check: Skipping activation on Web (requires ReCaptcha config).");
    }
    
    // Initialize notification service
    await NotificationService().initialize();
    
    // Initialize activity tracker
    ActivityTracker().initialize();
    
    // Ensure Master Admin Exists (Seeding)
    await AuthService().ensureMasterAdminExists();
    
  } catch (e, stack) {
    print("Firebase Initialization Error: $e");
    print("Stack Trace: $stack");
  }

  runApp(const ADACSApp());
}

