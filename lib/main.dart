/// This widget listens to the Auth State (Logged In vs Logged Out)
class AuthWrapper extends StatelessWidget {
  const AuthWrapper({super.key});

  @override
  Widget build(BuildContext context) {
    return StreamBuilder<User?>(
      stream: FirebaseAuth.instance.authStateChanges(),
      builder: (context, snapshot) {
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const Scaffold(body: Center(child: CircularProgressIndicator()));
        }

        if (snapshot.hasData && snapshot.data != null) {
          return const RoleResolver();
        }

        return const LoginScreen();
      },
    );
  }
}

/// Fetches the User's Role from Firestore to decide which Dashboard to show
class RoleResolver extends StatelessWidget {
  const RoleResolver({super.key});

  @override
  Widget build(BuildContext context) {
    final User user = FirebaseAuth.instance.currentUser!;

    return FutureBuilder<DocumentSnapshot>(
      future: FirebaseFirestore.instance.collection('users').doc(user.uid).get(),
      builder: (context, snapshot) {
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const Scaffold(
            body: Center(
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  CircularProgressIndicator(),
                  SizedBox(height: 20),
                  Text("Fetching Profile..."),
                ],
              ),
            ),
          );
        }

        if (snapshot.hasError || !snapshot.hasData || !snapshot.data!.exists) {
          return Scaffold(
            body: Center(
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  const Icon(Icons.lock_person_outlined, size: 60, color: Colors.red),
                  const SizedBox(height: 16),
                  const Padding(
                    padding: EdgeInsets.symmetric(horizontal: 40, vertical: 8),
                    child: Text(
                      "Please ensure your account is approved and Firestore rules allow access.",
                      textAlign: TextAlign.center,
                    ),
                  ),
                  const Text(
                    "A-DACS",
                    style: TextStyle(
                      fontSize: 28,
                      fontWeight: FontWeight.bold,
                      color: Color.fromARGB(255, 198, 55, 45), // CHANGED: Red Title
                      letterSpacing: 2,
                    ),
                  ),
                  const Text(
                    "Digital Clearance System",
                    style: TextStyle(fontSize: 14, color: Colors.grey),
                  ),
                  const SizedBox(height: 40),
                  TextButton(
                    onPressed: () => FirebaseAuth.instance.signOut(), 
                    child: const Text("Logout & Try Again")
                  ),
                ],
              ),
            ),
          );
        }

        final data = snapshot.data!.data() as Map<String, dynamic>;
        final String role = data['role'] ?? 'student';

        NotificationService().updateAppBadge();

        if (role == 'admin') {
          return const AdminDashboard();
        } else if (role == 'staff') {
          return const StaffDashboard();
        } else {
          return const StudentDashboard();
        }
      },
    );
  }
}